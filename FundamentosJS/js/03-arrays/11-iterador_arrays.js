// Iteradores de arrays

const techs = ['HTML', 'CSS', 'JavaScript', 'React', 'NodeJS'];

// forEach
const forEachArr = techs.forEach((tech) => {
    return tech;
})
// Unicamente lista los elementos

// map
const mapArr = techs.map((tech) => {
    return tech;
})
//ma crea un nuevo array mientras que forEach solo lo itera

console.log(forEachArr)
console.log(mapArr)
